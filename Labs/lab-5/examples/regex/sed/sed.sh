#!/bin/bash

# 1. Create a Practice Environment
mkdir -p Laborator5/practice
cd Laborator5/practice
echo -e "apple\nbanana\ncarrot\ndate\nelderberry" > groceries.txt

# 2. Replace Specific Characters
sed -i 's/a/@/g' groceries.txt

# 3. Find and Replace Using a Regular Expression
sed -i 's/\bc\w*/d/g' groceries.txt

# 4. Replace Vowels
sed -i 's/[aeiouAEIOU]/*/g' groceries.txt

# 5. Swap Words in a File
echo -e "Mary had a little lamb\nLittle lamb little lamb\nMary had a little lamb\nWhose fleece was white as snow" > mary.txt
sed -i 's/little lamb/lamb little/g' mary.txt

echo "All tasks completed successfully!"
